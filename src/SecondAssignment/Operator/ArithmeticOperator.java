package SecondAssignment.Operator;

public class ArithmeticOperator {

    public static void main(String[] args) {

        int  qty= 10;
        int price =2;
        System.out.println( qty+price);
        System.out.println( qty-price);
        System.out.println( qty*price);
        System.out.println( qty/price);
        System.out.println( qty%price);

    }
}
