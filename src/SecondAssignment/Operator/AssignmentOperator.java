package SecondAssignment.Operator;

public class AssignmentOperator {

    public static void main(String[] args) {

        int qty = 10;
        int price = 2;
        int totalQty;
        totalQty = qty;
        System.out.println(totalQty);

        System.out.println(price);

        price += 15;
        System.out.println(price);

        totalQty += 10;
        System.out.println(totalQty);

        System.out.println(qty);

    }
}
