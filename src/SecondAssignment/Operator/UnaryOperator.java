package SecondAssignment.Operator;

public class UnaryOperator {

    public static void main(String[] args) {

        int mobBill = 1000; //Declared a Local Variable
        System.out.println( mobBill++);
        System.out.println( ++mobBill);
        System.out.println( mobBill++);
        System.out.println(mobBill--);
        System.out.println(--mobBill);

    }
}
